def topic(
    title: str,
    angle: str,
    pillar: str,
    subtopic: str,
    article_type: str,
    *,
    audience: str | None = None,
    route_type: str = "broad",
    legal_complexity: str | None = None,
    overlap_group: str | None = None,
    style_profile: str | None = None,
    search_intent: str = "informational_problem_solving",
    scope_warning: str = "none",
    notes: list[str] | None = None,
) -> dict:
    default_audience, default_complexity, default_overlap = PILLAR_DEFAULT_META[pillar]
    if style_profile is None:
        style_profile = {
            "comparison_piece": "comparison_led",
            "evidence_strategy": "evidence_led",
            "procedural_strategy": "procedural",
            "risk_analysis": "risk_focused",
            "myth_correction": "myth_busting",
            "refusal_analysis": "case_led",
        }.get(article_type, "procedural")

    return {
        "topic": title,
        "angle": angle,
        "audience": audience or default_audience,
        "article_type": article_type,
        "pillar": pillar,
        "subtopic": subtopic,
        "route_type": route_type,
        "legal_complexity": legal_complexity or default_complexity,
        "overlap_group": overlap_group or default_overlap,
        "style_profile": style_profile,
        "search_intent": search_intent,
        "scope_warning": scope_warning,
        "notes_for_drafting": notes or [
            "Do not overstate availability of the route.",
            "Keep the analysis tied to the applicant's facts, evidence and procedural posture.",
            "Avoid generic checklist content where a route-specific explanation is needed.",
        ],
        "status": "unused",
    }


